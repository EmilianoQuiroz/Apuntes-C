//OPERADORES ARITMETICOS

/*
OPERADOR        NOMBRE        SIGNIFICADO        
   +            Aditivo       Suma
   -            Inverso       Resta
   *            Producto      Multiplicacion
   /            Cociente      Divicion
   %            Modulo        Residuo de una divicion
   ++           Incremento    Suma 1
   --           Decremento    Resta 1
*/

//OPERADORES RELACIONALES
/*
OPERADOR        SIGNIFICADO
   <            Menor que
   >            Mayor que
   <=           Menor o igual
   >=           Mayor o igual
   ==           Igual a
*/

//OPERADORES LOGICOS
/*
OPERADOR       NOMBRE        SIGNIFICADO
   !           NOT           Negacion 
   &&          AND           Insercion(Y)
   ||          OR            Union(O) 
*/

//OPERADORES DE ASIGNACION
/*
OPERADOR       SIGNIFICADO
   =           Asignacion
   +=          Suma y asignacion
   -=          Resta y asignacion
   *=          Multiplicacion y asignacion
   /=          Divicion y asignacion
   %=          Modulo y asignacion
*/